module.exports.sendResponse = (res, err, result) => {
  let responseData = {
    status: "",
    data: [],
    message: "",
  };
  if (err) {
    responseData.status = "error";
    responseData.data = null;
    responseData.message = err;
  } else {
    responseData.status = "success";
    responseData.data = result;
    responseData.message = null;
  }
  res.json(responseData);
};

module.exports.mergeDuplicateAttributes = (attributeArr) => {
  let mergedCharacteristicsArr = [];
  attributeArr.map((item) => {
    let foundAttribute;
    mergedCharacteristicsArr.map((findItem) => {
      if (Object.keys(findItem)[0] == Object.keys(item)[0]) {
        foundAttribute = findItem;
      }
    });

    if (foundAttribute) {
      if (
        foundAttribute[Object.keys(item)[0]].data.find(
          (el) => el == item[Object.keys(item)[0]].data[0]
        ) == undefined
      ) {
        foundAttribute[Object.keys(item)[0]].data.push(
          item[Object.keys(item)[0]].data[0]
        );
      }
    } else {
      mergedCharacteristicsArr.push(item);
    }
  });
  return mergedCharacteristicsArr;
};

module.exports.getCurrentDateTime = () => {
  var current = "";
  var today = new Date();
  var date =
    today.getFullYear() + "-" + (today.getMonth() + 1) + "-" + today.getDate();
  var time =
    today.getHours() + ":" + today.getMinutes() + ":" + today.getSeconds();
  current = date + " " + time;
  return current;
};

